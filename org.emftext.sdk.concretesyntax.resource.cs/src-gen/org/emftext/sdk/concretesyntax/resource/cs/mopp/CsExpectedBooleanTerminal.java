package org.emftext.sdk.concretesyntax.resource.cs.mopp;

/**
 * A representation for a range in a document where a boolean attribute is
 * expected.
 */
public class CsExpectedBooleanTerminal extends org.emftext.sdk.concretesyntax.resource.cs.mopp.CsAbstractExpectedElement {
	
	private org.emftext.sdk.concretesyntax.resource.cs.grammar.CsBooleanTerminal booleanTerminal;
	
	public CsExpectedBooleanTerminal(org.emftext.sdk.concretesyntax.resource.cs.grammar.CsBooleanTerminal booleanTerminal) {
		super(booleanTerminal.getMetaclass());
		this.booleanTerminal = booleanTerminal;
	}
	
	public org.emftext.sdk.concretesyntax.resource.cs.grammar.CsBooleanTerminal getBooleanTerminal() {
		return booleanTerminal;
	}
	
	private org.eclipse.emf.ecore.EStructuralFeature getFeature() {
		return booleanTerminal.getFeature();
	}
	
	public String toString() {
		return "EFeature " + getFeature().getEContainingClass().getName() + "." + getFeature().getName();
	}
	
	public boolean equals(Object o) {
		if (o instanceof CsExpectedBooleanTerminal) {
			return getFeature().equals(((CsExpectedBooleanTerminal) o).getFeature());
		}
		return false;
	}
	
	public java.util.Set<String> getTokenNames() {
		// BooleanTerminals are associated with two or one token(s)
		java.util.Set<String> tokenNames = new java.util.LinkedHashSet<String>(2);
		String trueLiteral = booleanTerminal.getTrueLiteral();
		if (!"".equals(trueLiteral)) {
			tokenNames.add("'" + trueLiteral + "'");
		}
		String falseLiteral = booleanTerminal.getFalseLiteral();
		if (!"".equals(falseLiteral)) {
			tokenNames.add("'" + falseLiteral + "'");
		}
		return tokenNames;
	}
	
}
